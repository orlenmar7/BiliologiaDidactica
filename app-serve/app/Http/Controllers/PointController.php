    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $user = auth()->user();

        $point = Point::where('test_id', $request->get('test_id'))
            ->where('user_id', $user->id)
            ->first();


        if ($point instanceof Point) {
            $point->fill($request->all());
            $point->save();
        }else{
            $user->points()->create($request->all());
        }

        return response()->json([
            'title' => 'Completado',
            'message' => "Puntos guardados con Ã©xito, ahora tienes " . $user->total_point . 'Puntos',
            'interceptor' => true,
            'plugin' => 'modal',
            'user' => $user->total_point
        ], 201);

    }

